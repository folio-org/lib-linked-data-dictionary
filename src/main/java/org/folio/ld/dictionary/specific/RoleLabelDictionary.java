  private RoleLabelDictionary() {
  }

  public static Optional<PredicateDictionary> getValue(String label) {
    return ofNullable(label)
      .map(RoleLabelDictionary::normalize)
      .map(VALUE_MAP::get);
  }

  private static String normalize(String text) {
    return NON_ALPHA_PATTERN.matcher(text)
      .replaceAll(EMPTY)
      .toLowerCase();
  }
